public class Player {

	public String name;
	public String team;
	public int salary;
	public String pos;
	public double value;
	public double score;
	
	
	public Player(String name, String pos,int salary,String team,double value,double score) {
		this.name = name;
		this.team = team;
		this.salary = salary;
		this.pos = pos;
		this.value = value;
		this.score = score;
	}
}